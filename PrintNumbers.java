package corejava;

/**
 * This program is for
 * print 1 to 10 number
 * @author macbook
 */
public class PrintNumbers {

	/*
	 * This method is for
	 * print 1 to 10 number
	 * using do while loop
	 */
	public static void main(String[] args) {
		int i = 1;
		do {
			System.out.println(+ i);
			i++;
		} while (i <= 10);
	}

}
