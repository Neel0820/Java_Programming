package corejava;

/**
 * This program is for
 * print 1 to 10 number
 * @author macbook
 */
public class PrintNumbers {

	/*
	 * This method for
	 * print 1 to 10 number
	 * using do while
	 */
	public static void main(String[] args) {
		int i = 1;
		do {
			System.out.println(+ i);
			i++;
		}// End of do statement
		while (i <= 10);// End of while statement
	}

}
